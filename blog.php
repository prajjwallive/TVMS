                    <p class="mb-4 text-base text-gray-700 md:text-lg text-justify">
                        ISKCON, which stands for the International Society for Krishna Consciousness, is a religious
                        organization that follows the teachings and practices of the Vaishnava tradition within
                        Hinduism. People who are associated with ISKCON, often referred to as devotees or members,
                        engage in various activities as part of their spiritual practice. Here are some common
                        activities that people do in ISKCON:

                    <h1>Activities in ISKCON</h1>
                    <h2>1. Devotional Worship</h2>
                    <p>Devotees engage in devotional practices centered around the worship of Lord Krishna.</p>

                    <h2>2. Study of Sacred Texts</h2>
                    <p>Devotees regularly study sacred texts like the Bhagavad Gita and the Srimad Bhagavatam.</p>

                    <h2>3. Sankirtan (Congregational Chanting)</h2>
                    <p>Devotees participate in sankirtan, chanting the Hare Krishna mantra individually or in groups.
                    </p>

                    <h2>4. Preaching and Outreach</h2>
                    <p>Devotees engage in preaching activities, such as distributing books and giving lectures.</p>

                    <h2>5. Seva (Service)</h2>
                    <p>Devotees offer selfless service within the temple community in various roles and tasks.</p>

                    <h2>6. Festivals and Celebrations</h2>
                    <p>ISKCON celebrates festivals with ceremonies, processions, music, dance, and feasting.</p>

                    <h2>7. Spiritual Education and Training</h2>
                    <p>ISKCON provides spiritual education and training through courses, seminars, and workshops.</p>
